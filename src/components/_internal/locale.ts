export type LocaleKey = keyof typeof LOCALE_PT_BR;

let _activeLocale: Partial<Record<LocaleKey, string>> = {};

export function setLocale(locale: Record<string, string>): void {
  _activeLocale = locale;
}

export function setLocaleByLang(lang: 'pt-BR' | 'en' | string): void {
  if (lang === 'en') {
    _activeLocale = LOCALE_EN as unknown as Record<string, string>;
  } else {
    _activeLocale = {};
  }
}

export function t(key: LocaleKey, params?: Record<string, string>): string {
  let val = _activeLocale[key] ?? LOCALE_PT_BR[key];
  if (params) {
    val = val.replace(/\$\{(\w+)\}/g, (_, p) => params[p] ?? `${p}`);
  }
  return val;
}

